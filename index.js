function App(){
 
    const [count, setCount] = React.useState(0);
    const [change, setChange] = React.useState("#16a085");
    function newQoute(){
        let colors = [
            '#16a085',
            '#27ae60',
            '#2c3e50',
            '#f39c12',
            '#e74c3c',
            '#9b59b6',
            '#FB6964',
            '#342224',
            '#472E32',
            '#BDBB99',
            '#77B1A9',
            '#73A857'
          ];
        let randomQuote = Math.floor(Math.random() * quotes.length);
        setCount(randomQuote);
        let randomColer = Math.floor(Math.random() * colors.length);
        setChange(colors[randomColer]);
    }
    return(
        <div style={{
            backgroundColor: change,
            width: '100vw',
            height: '100vh'
          }}>
            <div className ="square" id="quote-box">
                <div className = "quote" id="text"> 
                    "{quotes[count].text}"
                </div>
                <p className = "author" id="author">
                    -{quotes[count].source}
                </p>
                <a href={`https://twitter.com/intent/tweet?hashtags=&related=&text="${quotes[count].qoute}"-${quotes[count].author}`}id="tweet-quote">
                    <button>Tweet</button>
                </a>
                <button className="right" id="new-quote" onClick={newQoute}>
                    New quote
                </button>
            </div>
        </div>
    )
}

const root = ReactDOM.createRoot(
    document.getElementById('root')
);
root.render(<App />);